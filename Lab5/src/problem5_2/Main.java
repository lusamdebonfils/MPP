package problem5_2;

public class Main {

	public static void main(String[] args) {
		double sumOfAreas = 0;
		Figure[] figures = {new Triangle(3,4), new Rectangle(4, 3), new Circle(7)};
		
		for(Figure f: figures) {
			sumOfAreas += f.computeArea();
		}
		System.out.println("Sum of areas: " + sumOfAreas);
	}

}
